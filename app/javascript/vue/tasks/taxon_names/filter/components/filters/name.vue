<template>
  <div>
    <h2>Taxon</h2>
    <div class="field">
      <label>Name</label>
      <input
        type="text"
        placeholder="Name"
        class="full_width"
        @keyup.enter="$emit('onSearch')"
        v-model="taxon.name">
    </div>
    <div class="field">
      <label>Author</label>
      <input
        type="text"
        class="full_width"
        placeholder="Author"
        @keyup.enter="$emit('onSearch')"
        v-model="taxon.author">
    </div>
    <div class="field">
      <label>Year</label>
      <input
        class="field-year"
        type="text"
        placeholder="Year"
        @keyup.enter="$emit('onSearch')"
        v-model="taxon.year">
    </div>
  </div>
</template>

<script>

import { URLParamsToJSON } from 'helpers/url/parse.js'

export default {
  props: {
    value: {
      type: Object,
      required: true
    }
  },
  computed: {
    taxon: {
      get () {
        return this.value
      },
      set (value) {
        this.$emit('input', value)
      }
    }
  },
  mounted () {
    const params = URLParamsToJSON(location.href)
    this.taxon.name = params.name
    this.taxon.author = params.author
    this.taxon.year = params.year
  }
}
</script>

<style lang="scss" scoped>
  .field {
    label {
      display: block;
    }
  }
  .field-year {
    width: 60px;
  }

</style>
